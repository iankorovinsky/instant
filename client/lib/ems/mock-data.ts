// Helper functions
export function getExecutionWithFills(executionId: string): ExecutionWithFills | undefined {
  const execution = executions.find((e) => e.executionId === executionId);
  if (!execution) return undefined;

  const executionFills = fills.filter((f) => f.executionId === executionId);
  return { ...execution, fills: executionFills };
}

export function getExecutionEvents(executionId: string): ExecutionEvent[] {
  return executionEvents.filter((e) => e.executionId === executionId);
}

export function getExecutionSummary(): ExecutionSummary {
  const filledExecutions = executions.filter((e) =>
    e.status === "FILLED" || e.status === "SETTLED"
  );

  const totalSlippage = filledExecutions.reduce((sum, e) =>
    sum + (e.slippageBreakdown?.total || 0), 0
  );

  return {
    total: executions.length,
    pending: executions.filter((e) => e.status === "PENDING").length,
    simulating: executions.filter((e) => e.status === "SIMULATING").length,
    partiallyFilled: executions.filter((e) => e.status === "PARTIALLY_FILLED").length,
    filled: executions.filter((e) => e.status === "FILLED").length,
    settled: executions.filter((e) => e.status === "SETTLED").length,
    cancelled: executions.filter((e) => e.status === "CANCELLED").length,
    totalFilledQuantity: executions.reduce((sum, e) => sum + e.filledQuantity, 0),
    totalSlippageBp: totalSlippage,
    avgSlippageBp: filledExecutions.length > 0 ? totalSlippage / filledExecutions.length : 0,
  };
}

export function getStateColor(status: ExecutionState): string {
  switch (status) {
    case "PENDING":
      return "bg-blue-100 text-blue-800";
    case "SIMULATING":
      return "bg-blue-100 text-blue-800";
    case "PARTIALLY_FILLED":
      return "bg-yellow-100 text-yellow-800";
    case "FILLED":
      return "bg-green-100 text-green-800";
    case "SETTLED":
      return "bg-green-100 text-green-800";
    case "CANCELLED":
      return "bg-gray-100 text-gray-800";
    default:
      return "bg-gray-100 text-gray-800";
  }
}

export function formatCurrency(value: number): string {
  return new Intl.NumberFormat("en-US", {
    style: "currency",
    currency: "USD",
    minimumFractionDigits: 0,
    maximumFractionDigits: 0,
  }).format(value);
}

export function formatQuantity(value: number): string {
  return new Intl.NumberFormat("en-US").format(value);
}

export function formatDate(date: Date): string {
  return new Intl.DateTimeFormat("en-US", {
    year: "numeric",
    month: "short",
    day: "numeric",
  }).format(date);
}

export function formatDateTime(date: Date): string {
  return new Intl.DateTimeFormat("en-US", {
    year: "numeric",
    month: "short",
    day: "numeric",
    hour: "2-digit",
    minute: "2-digit",
    second: "2-digit",
  }).format(date);
}

export function formatPrice(value: number): string {
  return value.toFixed(4);
}

export function formatBasisPoints(value: number): string {
  return `${value.toFixed(1)}bp`;
}
